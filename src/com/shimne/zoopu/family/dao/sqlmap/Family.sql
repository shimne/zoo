	preface VARCHAR(500) NOT NULL DEFAULT '',
	origin VARCHAR(500) NOT NULL DEFAULT '',
	teaching VARCHAR(500) NOT NULL DEFAULT '',
	seniority VARCHAR(500) NOT NULL DEFAULT '',
	status TINYINT(1) NOT NULL DEFAULT 0,
	creatorId BIGINT(20) NOT NULL DEFAULT 0,
